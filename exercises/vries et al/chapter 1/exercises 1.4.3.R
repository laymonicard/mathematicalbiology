# Structured populations
# (a) Give examples of spatially structured problems. What kind of effects cannot be understood without spatial structure? 

# (b) Give an example of an age-structured problem

# (c) Give an example of a size-structured problem

# For all of the questions above, we define the structure of the problem when the structure explains the outcome we are interested in.
# For (a), movement of populations across different spatial units is an example of a spatially structured problems. 
# Migration is one example. In the context of infectious disease modelling, 
# infections can be driven by travels to other cities or other countries. 
# Another example is spatial autocorrelation, where nearby areas have more similar characteristics than the ones farther away. 
# Areas where the risk of natural disasters such as earthquake is higher can be defined by certain spatial features, such as distance to tectonic plates. 


# For (b)
# Some infectious diseases attack people from certain age group more than the others (e.g., due to contact patterns). 
# COVID-19 is one of such infections. 

# For (c)
# Body size determines wing size of an animal. Larger body size requires larger wings and the relationship is defined by a square-cubed law. 
# Population size drive food demands. Food security problem can be an example of a size-structured problem, although "size" here does not refer to physical size but more about quantity. 
# Some people also mention cell division as a size-structured problem.